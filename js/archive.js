var logo = document.getElementById("logo");

var logoUrls = [
  "../Images/Logos/ErikMerisalu-Website-Logo-1.png",
  "../Images/Logos/ErikMerisalu-Website-Logo-2.png"
]

var currentLogoIndex = 0;

function changeLogo() {
  logo.src = logoUrls[currentLogoIndex];
  currentLogoIndex = (currentLogoIndex + 1) % logoUrls.length;
}

changeLogo();
setInterval(changeLogo, 1000)


for (var i = 0; i < risoImagePaths.length; i++) {
  var imgDiv = document.createElement("div");
  imgDiv.className = "article";
  imgDiv.id = risoImagePaths[i].id;

  var img = document.createElement("img");
  img.src = risoImagePaths[i].path;

  imgDiv.appendChild(img);
  imgContainer.appendChild(imgDiv);
}

var targetImages = document.querySelectorAll("#feed .article img");

var options = {
  root: null,
  rootMargin: "0px",
  threshold: 0.5
};

var observer = new IntersectionObserver(function (entries, observer) {
  entries.forEach(function (entry) {
    if (entry.isIntersecting) {
      var index = Array.from(targetImages).indexOf(entry.target);
      var paramL = risoImagePaths[index].textLeft;
      var paramR = risoImagePaths[index].textRight;

      txtElementL.innerHTML = '';
      txtElementR.innerHTML = '';

      if (paramL) {
        paramL.forEach(function (text) {
          var p = document.createElement("p");
          p.textContent = text;
          txtElementL.appendChild(p);
        });
      }

      if (paramR) {
        paramR.forEach(function (text) {
          var p = document.createElement("p");
          p.textContent = text;
          txtElementR.appendChild(p);
        });
      }
    }
  });
}, options);

targetImages.forEach(function (image) {
  observer.observe(image);
});